package org.startupfactory.exercise5.hogwarts;



public class Human  {
	private String names;
	private int age;
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public Human(String names, int age) {
		super();
	this.names=names;
	this.age=age;
	}

}
